#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst)
		return ;
	while (*lst)
	{
		temp = *lst;
		(*del)(temp->content);
		*lst = (*lst)->next;
		free(temp);
	}
}
